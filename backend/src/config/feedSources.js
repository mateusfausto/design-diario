export const getFeedsByCategory = () => {
  const grouped = {};
  defaultFeeds.forEach(feed => {
    if (!grouped[feed.category]) {
      grouped[feed.category] = [];
    }
    grouped[feed.category].push(feed);
  });
  return grouped;
};

export const getCategories = () => {
  return [...new Set(defaultFeeds.map(feed => feed.category))].sort();
};
